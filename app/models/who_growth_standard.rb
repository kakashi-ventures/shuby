  # Look up LMS values for a specific sex, measurement type, and age
  # Returns nil for unsupported combinations
  def self.lms_for(sex:, type:, age_months:)
    return nil unless sex.in?([:male, :female])
    return nil unless type.in?([:weight, :height, :head_circumference])

    age = age_months.floor.clamp(0, 36)
    WHO_LMS_DATA.dig(sex, type, age)
  end

  # Reference ranges (p3, median, p97) for all measurement types at a given age.
  # Used by the Timeline page to show average measurements.
  # Returns nil if sex is not male/female.
  def self.reference_ranges(sex:, age_months:)
    return nil unless sex.in?([:male, :female])

    age = age_months.floor.clamp(0, 36)
    %i[weight height head_circumference].each_with_object({}) do |type, result|
      lms = WHO_LMS_DATA.dig(sex, type, age)
      next unless lms

      result[type] = {
        p3: value_from_lms(lms, PERCENTILE_Z_SCORES[3]).round(1),
        median: value_from_lms(lms, 0.0).round(1),
        p97: value_from_lms(lms, PERCENTILE_Z_SCORES[97]).round(1)
      }
    end
  end

  # Generate percentile curve data for chart rendering
  # Returns array of hashes: [{ month:, p3:, p10:, p25:, p50:, p75:, p90:, p97: }, ...]
  def self.percentile_curves(sex:, type:, from_month: 0, to_month: 36)
    return [] unless sex.in?([:male, :female])
    return [] unless type.in?([:weight, :height, :head_circumference])

    (from_month..to_month).filter_map do |month|
      lms = WHO_LMS_DATA.dig(sex, type, month)
      next unless lms

      curve_point = {month: month}
      PERCENTILE_Z_SCORES.each do |percentile, z|
        curve_point[:"p#{percentile}"] = value_from_lms(lms, z).round(4)
      end
      curve_point
    end
  end

  # Calculate the measurement value at a given z-score using LMS parameters
  # Inverse of the Box-Cox transformation
  def self.value_from_lms(lms, z_score)
    l, m, s = lms.values_at(:l, :m, :s)
    if l.zero?
      m * Math.exp(s * z_score)
    else
      m * ((1 + l * s * z_score)**(1.0 / l))
    end
  end
